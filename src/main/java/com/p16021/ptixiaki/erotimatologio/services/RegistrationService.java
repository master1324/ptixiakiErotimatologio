    public void register(RegistrationRequest request){

        boolean isValidEmail = emailValidator.test(request.getEmail());

        if(!isValidEmail){
            log.error("EMAIL NOT VALID");
            throw new IllegalStateException("email not valid");
        }

        String token = userService.register(new AppUser(
                request.getUsername(),
                passwordEncoder.encode(request.getPassword()),
                request.getEmail()));

        //TODO : get link from front end
        String l = "http://localhost:8080/confirm?token="+ token;
        emailSender.send(request.getEmail(),buildEmail(request.getUsername(),l),"Epibebeosi email");

        //return token;
    }

    @Transactional
    public String confirmToken(String token) {
        ConfirmationToken confirmationToken = conformationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("token not found"));

        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("email already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("token expired");
        }

        conformationTokenService.setConfirmedAt(token);
        userService.enableAppUser(
                confirmationToken.getAppUser().getEmail());

        return "confirmed";
    }

